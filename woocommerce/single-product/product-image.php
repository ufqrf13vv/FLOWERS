<div class="product__item-image_block">
	<div class="product__item-image" id="product__main-image">
		<img src="<?php echo $full_size_image[0]; ?>" alt="<?php echo $product->name; ?>">
	</div>
	<div class="product__item-wrapper product__item-wrapper--product">
		<?php
		do_action( 'woocommerce_product_thumbnails' );
		?>
